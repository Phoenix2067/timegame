package com.rewinder;

/**
 * Basic x/y point.
 */
public class Vector2 {
    public double x;
    public double y;

    /**
     * Creates a 2D vector.
     *
     * @param x the x coordinate
     * @param y the y coordinate
     */
    public Vector2(double x, double y) {
        this.x = x;
        this.y = y;
    }
}
